'use strict';

var VError = require('verror').VError;
var xsrfHandler = require('./xsrf-token-handler');
const logoutProvider = require('./logout-provider');

module.exports = function xsrfCheck(req, res, next) {
  if (!xsrfHandler.isTokenRequired(req)) {
    return next();
  }

  if (xsrfHandler.isTokenFetchRequest(req)) {
    xsrfHandler.getToken(req, function (err, token) {
      if (err) {
        return next(new VError(err, 'Failed to get new xsrf token'));
      }
      res.setHeader('x-csrf-token', token);
      if (logoutProvider.isLogoutRequest(req)){
        res.status = 200;
        res.end();
      }
      return next();
    });
  } else {
    var result = xsrfHandler.checkToken(req);
    if (result.isValid) {
      return next();
    }
    var error = new Error(result.details);
    error.status = 403;
    error.headers = { 'x-csrf-token': 'Required' };
    next(error);
  }
};
